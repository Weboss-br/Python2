#Manipulação de String:

nomeCompleto = '              Dieter Heiss'

print ('Maiúscula: ', nomeCompleto.upper(), '\n')

print ('Minuscula: ', nomeCompleto.lower(), '\n')

print ('Remover espaços de antes ou depois da string: ', nomeCompleto.strip(), '\n')

print ('Replace: ', nomeCompleto.replace('e', '@'), '\n')

print ('Fatiar: ', nomeCompleto.split(','), '\n')
