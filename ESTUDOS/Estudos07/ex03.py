#Manipulação de String. Contagem de caracteres:

nomeCompleto = 'Dieter Heiss'
#Para iniciar com a primeiro, use 0 ou nada:
print ('Primeiro nome: ',nomeCompleto[:6])
print ('Segundo nome: ',nomeCompleto[6:])

#Contagem de trás para frente:
print('\nContagem de trás para frente: ',nomeCompleto[-5:-1])